let tool = {
	getCallerStackTrace() {
		let trace = Error.prepareStackTrace;
		Error.prepareStackTrace = (trace, stack) => stack;
		let stack = new Error().stack.slice(1);
		Error.prepareStackTrace = trace;
		return stack;
	}
};

let util = {
	isServer: true,
	setSourcePaths(info) {
		if (info.style || info.template) {
			let basePath = tool.getCallerStackTrace()[2].getFileName();
			if (basePath) {
				info.module = basePath;
				if (info.style) {
					info.style = { path: Path.resolve(basePath, "./../", info.style) };
				}
				if (info.template) {
					info.template = { path: Path.resolve(basePath, "./../", info.template) }
				}
			}
		}
	},
	isRegularTag(tagName) {
		tagName = tagName.trim();
		let t = TAGS.includes(tagName);
		if (!t) {
			t = SVGTAGS.includes(tagName);
		}
		return t;
	}
};

module.exports = util;